// Criei uma variavel inicial valendo 1.
let inicio = 0;
let fim = 10;

// Realizo o looping while/enquanto.
while (inicio <= fim) {
    // Faco a minha logica aqui.
    console.log(inicio);
    inicio++;
}

// Depois que while, terminou. Ou seja, vai para a proxima
// Linha
console.log("terminou");